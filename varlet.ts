//var keyword
var greeter = "hey hi";  
var times = 5;  
if (times > 3) {  
   var greeter = "Say Hello";   
}  
console.log(greeter) //Output: Say Hello  

//let keyword
let greeter = "hey hi";  
let times = 5;  
if (times > 3) {  
   let hello = "Say Hello ";   
   console.log(hello) // Output: Say Hello 
}  
